    public class IUserPaginatedVM
    {
        public IEnumerable<AcountViewModel> IuserList { get; set; }
        public int ItemPerPage { get; set; }
        public int CurrentPage { get; set; }

        public int PageCount()
        {
            return Convert.ToInt32(Math.Ceiling(IuserList.Count() / (double)ItemPerPage));
        }
        public IEnumerable<AcountViewModel> PaginatedList()
        {
            int start = (CurrentPage - 1) * ItemPerPage;
            return IuserList.OrderByDescending(b => b.UserName).Skip(start).Take(ItemPerPage);
        }
    }


